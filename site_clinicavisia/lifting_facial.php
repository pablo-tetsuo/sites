		<h3>O ato cirúrgico do lifting</h3>
		<p>
		Realizado com incisões mínimas ao ponto de tratar somente a área de incomodo, em si demora aproximadamente 1h para cada região a ser tratada. As incisões são feitas na região anterior e posterior da orelha continuando-se dentro do couro cabeludo. Através delas a pele é descolada superficialmente e tratamos o tecido imediatamente abaixo o tracionando. O excesso de pele então é removido. Desta maneira tratamos a face e o pescoço. É uma cirurgia que requer muita atenção e cuidado por parte do cirurgião.
